    node.quoteUniCode = function(str) {
        var i = 0;
        var chars = '';
        while (str.charAt(i)) {
            if (str.charCodeAt(i + 1) > 55295) {
                chars += '<span class="symbol">' + str.charAt(i++) + str.charAt(i++) + str.charAt(i++) + str.charAt(i++) + '</span>';
            }
            else {
                chars += str.charAt(i++);
            }
        }
        return chars;
    };
    node.listEvents = function(auth, callback) {
        var calendar = google.calendar('v3');
        calendar.events.list({
            auth: auth,
            calendarId: 'e2ip0q5os1qa16reerejcvsrd0@group.calendar.google.com',
            timeMin: (new Date()).toISOString(),
            maxResults: 10,
            singleEvents: true,
            orderBy: 'startTime'
        }, 'UTF-8', function(err, response) {
            if (err) {
                if (callback != null) {
                    callback('<span class="s404">Kalender konnte nicht geladen werden</span>');
                }
                return;
            }
            var events = response.items;
            if (events.length > 0) {
                var days = {};
                for (var i = 0; i < events.length; i++) {
                    var event = events[i];
                    var startDate;
                    var day = null;
                    var time = null;
                    if ("dateTime" in event.start) {
                        startDate = new Date(event.start.dateTime);
                        time = nll(startDate.getHours()) + ':' + nll(startDate.getMinutes());
                    }
                    else {
                        startDate = new Date(event.start.date);
                    }
                    day = nll(startDate.getDate()) + '.' + nll(startDate.getMonth() + 1) + '.' + (startDate.getYear()-100);
                    if (days[day] == null) {
                        days[day] = {};
                    }
                    if (!('list' in days[day])) {
                        days[day].list = [];
                    }
                    if (!('times' in days[day])) {
                        days[day].times = {};
                    }
                    var location = null;
                    if (event.location != undefined) {
                        location = node.quoteUniCode(event.location.split(',')[0]);
                    }
                    if ("dateTime" in event.start) {
                        days[day].times[time] = {
                            text: node.quoteUniCode(event.summary),
                            loc: location
                        };
                    }
                    else {
                        days[day].list.push({
                            text: node.quoteUniCode(event.summary),
                            loc: location
                        });
                    }
                }
                var s = '';
                /*var counter = 0;
                var max = 6;*/
                for (var day in days)
                /*if (counter+1 < max) {
                    counter++;*/
                {
                    s += '<span class="gcDay">' + day + '</span> ';
                    if (days[day].list.length > 0) {
                        days[day].list.forEach(function(e) {
                            /*if (counter < max) {
                                counter++;*/
                            s += '<span class="gcEvent">' + e.text + '</span> ';
                            if (e.loc != null)
                                s += '<span class="symbol">&#127757;&nbsp;</span><span class="gcLocation">' + e.loc + '</span > ';
                                //}
                        });
                    }
                    if (Object.keys(days[day].times).length > 0) {
                        Object.keys(days[day].times).forEach(function(e) {
                            /*if (counter < max) {
                                counter++;*/
                            s += '<span class="gcTime">' + e + '</span> ';
                            s += '<span class="gcEvent">' + days[day].times[e].text + '</span> ';
                            if (days[day].times[e].loc != null)
                                s += '<span class="world">&#127757;&nbsp;</span><span class="gcLocation">' + days[day].times[e].loc + '</span > ';
                                //}
                        });
                    }
                    s += '<br>';
                }
                if (callback != null) {
                    callback(s);
                }
            }
        });
    };

    node.googleCalendar = function(callback) {
        if (node.credentials == null) {
            callback('<span class="s404">please wait!</span>');
            return;
        }
        var clientSecret = node.credentials.installed.client_secret;
        var clientId = node.credentials.installed.client_id;
        var redirectUrl = node.credentials.installed.redirect_uris[0];
        var auth = new googleAuth();
        var oauth2Client = new auth.OAuth2(clientId, clientSecret, redirectUrl);

        // Check if we have previously stored a token.
        fs.readFile(TOKEN_PATH, function(err, token) {
            if (!err) {
                oauth2Client.credentials = JSON.parse(token);
                node.listEvents(oauth2Client, callback);
            }
        });
    };

